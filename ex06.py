import math

print('___Sobre: O Dobro, Triplo e Raiz Quadrada___\n')
a = int(input('Digite um número: '))
print(f'\nO dobro de {a} vale {2*a}')
print(f'O triplo de {a} vale {3*a}')
print(f'A raiz quadrada de {a} vale {math.sqrt(a):.2f}')


